public class NetworkManager
{
  private String HOST;
  private int PORT_NUM;

  public NetworkManager()
  {

  }

  public String getHost()
  {
    return HOST;
  }

  public void setHost(String HOST)
  {
    this.HOST = HOST;
  }

  public int getPortNum()
  {
    return PORT_NUM;
  }

  public void setPortNum(int PORT_NUM)
  {
    this.PORT_NUM = PORT_NUM;
  }
}
